SELECT E.EVENTNO, E.DATEHELD, COUNT(EP.PLANNO) AS 'EVENT PLAN'
FROM EVENTREQUEST AS E, EVENTPLAN AS EP
WHERE WORKDATE LIKE '2018-12-%'
GROUP BY EVENTNO, DATEHELD;

-- 2 
SELECT EP.PLANNO, EP.EVENTNO, EP.WORKDATE, EP.ACTIVITY
FROM FACILITY AS F, EVENTPLAN AS EP
WHERE EP.WORKDATE LIKE '2018-12-%' AND F.FACNAME = 'Basketball arena';

-- 3
SELECT E.EVENTNO, E.DATEHELD, E.STATUS, E.ESTCOST
FROM EVENTREQUEST AS E, EMPLOYEE AS EM, FACILITY AS F
WHERE F.FACNAME = 'Basketball arena' AND EM.EMPNAME = 'Mary Manager' AND E.DATEHELD BETWEEN '2018-10-01' AND '2018-12-31';

-- 4
SELECT EP.PLANNO, EP.LINENO, R.RESNAME, EP.NUMBERFLD, F.FACNAME, EP.TIMESTART, EP.TIMEEND
FROM EVENTPLANLINE AS EP, FACILITY AS F, EVENTPLAN AS E, RESOURCETBL AS R
WHERE F.FACNAME = 'Basketball arena' AND E.ACTIVITY = 'Operation' AND E.WORKDATE BETWEEN '2018-10-01' AND '2018-12-31';

